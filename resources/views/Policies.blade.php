  <script>
    function showPolicies(category) {
      const allPolicies = document.querySelectorAll('.policies-list');
      const categoryBtns = document.querySelectorAll('.category-btn');

      // Hide all policy lists
      allPolicies.forEach(list => list.classList.add('hidden'));

      // Remove active class from all buttons
      categoryBtns.forEach(btn => btn.classList.remove('active'));

      // Show the selected category and add active class to the button
      document.getElementById(`${category}-policies`).classList.remove('hidden');
      document.querySelector(`.category-btn[onclick="showPolicies('${category}')"]`).classList.add('active');
    }
